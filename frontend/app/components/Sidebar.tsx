function navActive(
  pathname: string,
  hash: string,
  activeMatch: "dashboard" | "recommendations" | "agents" | "team" | "settings"
): boolean {
  switch (activeMatch) {
    case "dashboard":
      return pathname === "/" && hash !== "#recommendations";
    case "recommendations":
      return pathname === "/" && hash === "#recommendations";
    case "agents":
      return pathname.startsWith("/agents");
    case "team":
      return pathname === "/settings" && hash === "#organization";
    case "settings":
      return pathname === "/settings" && hash !== "#organization";
    default:
      return false;
  }
}

